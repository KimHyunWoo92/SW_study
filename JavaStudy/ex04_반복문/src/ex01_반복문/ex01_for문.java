package ex01_반복문;

public class ex01_for문 {

	public static void main(String[] args) {
		/*
		 반복문
		 	: 어떤 조건에 만족할 때 까지 특정실행문장(코드)를 반복하여 실행하는 구조
		 	for문
		 	: 주로 반복횟수가 정해진 경우에 사용!
		 	for(초기화구몬; 조건식; 반복후 작업) {
		 		조건식 겨로가가 true 일때만 실행됨
		 		실행문장;
		 		}
		 	(시작점; 목적지(종점); 보폭
		 */
		for (int i=96; i>=53;i--) {
			System.out.println(i);
		}
	}

}
